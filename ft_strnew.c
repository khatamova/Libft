#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*dst;
	size_t	i;

	i = 0;
	dst = malloc(size + 1);
	while (dst && i <= size)
	{
		dst[i] = 0;
		i++;
	}
	return (dst);
}
